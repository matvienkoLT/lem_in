int	ft_dgt_quntt_i(int n)
{
	int quantt;

	if (n == 0)
		return (1);
	quantt = 0;
	if (n < 0)
	{
		n *= (-1);
		quantt++;
	}
	while (n > 0)
	{
		n /= 10;
		quantt++;
	}
	return (quantt);
}
